//Um primitivo (valor primitivo, tipo de dados primitivo) é um dado que não é representado através de um Objeto e, por consequência, não possui métodos.

//tipos primtivos: String, Number, Boolean, Null, Undefined, Symbol(Novo no EcmaScript)
//todos os primitivos são imutáveis.

var a = 15;
console.log(typeof a);

var b = undefined;
console.log(typeof b);

var c = false;
console.log(typeof c);

var d = "Bom dia";
console.log(typeof d);

